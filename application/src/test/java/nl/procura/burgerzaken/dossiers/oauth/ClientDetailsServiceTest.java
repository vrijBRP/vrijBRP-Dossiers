package nl.procura.burgerzaken.dossiers.oauth;

import static java.util.Collections.emptySet;
import static org.junit.jupiter.api.Assertions.assertFalse;
import static org.junit.jupiter.api.Assertions.assertTrue;

import org.junit.jupiter.api.Test;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;

@SpringBootTest
class ClientDetailsServiceTest {

  @Autowired
  private ClientDetailsService service;

  @Test
  void createMustFailOnDuplicateId() {
    // given
    Client client = new Client("a", "secret", emptySet(), emptySet());
    assertTrue(service.create(client));
    Client duplicateClient = new Client("a", "anothersecret", emptySet(), emptySet());
    // when
    boolean created = service.create(duplicateClient);
    // then
    assertFalse(created);
  }
}
